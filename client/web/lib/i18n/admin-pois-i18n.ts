export function getAdminPoisBundle(lang: LanguageCode): AdminPoisBundle {
    return BUNDLES[lang] ?? BUNDLES.en
}

export function formatPoiDeleteDescription(bundle: AdminPoisBundle, name: string): string {
    return bundle.delete.description.replaceAll("{name}", name)
}

export function formatStripCount(strip: AdminPoisBundle["strip"], count: number): string {
    return strip.countSuffix ? `${count}${strip.found}` : `${count} ${strip.found}`
}

